      <p>L&auml;ngst ist Linux kein System mehr ausschlie&szlig;lich f&uuml;r
        IT-Profis, sondern l&auml;sst sich ebenso leicht wie Windows
        installieren und bringt dar&uuml;ber hinaus noch Software f&uuml;r
        viele Anwendungsbereiche und Benutzergruppen mit.
      </p>
      <p>Kinder und Jugendliche k&ouml;nnen mit Linux ihre ersten,
        lehrreichen Schritte am Computer mit Linux machen und dabei mit
        Spa&szlig; Rechnen und Lesen uvm. lernen.</p> 
      <p>Jugendlichen, Auszubildenden und Studierenden bietet das System
        viele Unterst&uuml;tzungsm&ouml;glichkeiten wie vorinstalliertes
        Officepaket, Kommunikation via eMail, Chat und Browser. Allgemein an
        Computerthemen Interessierte k&ouml;nnen unter Linux eine
        gro&szlig;e Anzahl von Programmiersprachen ausprobieren und sich an
        der Entwicklung von Linux und einzelner Programme beteiligen.</p>

