<h1>Wer sind wir?</h1>

Die Linux User Group Tübingen (LUGT) ist ein loser Zusammenschluss von Leuten, die Interesse am
Betriebssystem Linux haben und dieses anwenden oder benutzen möchten. Wir treffen uns regelmäßig
einmal im Monat, um Erfahrungen auszutauschen, Neuigkeiten auf der Linux-Welt in Erfahrung zu bringen
oder einfach nur um zusammenzusitzen und Klatsch und Tratsch auszutauschen.<br />
<br />
Zwischen den Treffen kennen wir uns vornehmlich als E-Mail-Adressen, die über die Mailingliste der
LUGT Neuigkeiten austauschen oder Hilfestellung leisten bei Problemen rund um Linux. Von Zeit zu Zeit
organisieren wir darüber hinaus Ausstellungen, Seminare oder Vortragsreihen.<br />
<br />
Alles in allem hoffen wir, kein Freakverein zu sein, sondern die LUGT ist explizit auch dafür da, um
nicht so versierten Linux-Anwendern Hilfen und Anregungen zu geben. <br/ >

