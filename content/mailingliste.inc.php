<h1>Mailingliste</h1>

Die LUGT betreibt eine Mailingliste zum Thema Linux. Wer ihr beitreten möchte kann das unter <a
href="https://listserv.uni-tuebingen.de/mailman/listinfo/lug-tuebingen">https://listserv.uni-tuebingen.de/mailman/listinfo/lug-tuebingen</a>
tun.<br /> <br />E-Mails an die Liste schickt man an <a
href="mailto:lug-tuebingen(at)listserv.uni-tuebingen.de">lug-tuebingen&#64;listserv.uni-tuebingen.de</a>. Es
empfiehlt sich aber Mitglied zu sein, bevor man E-Mails dorthin schickt, da man sonst die Antworten
nicht lesen kann, da diese im Normalfall ebenfalls auf die Liste geschickt werden. E-Mails von
Nicht-Mitgliedern werden erst von Hand approved - also nicht wundern wenn es ein paar Minuten oder
Stunden braucht bis die E-Mail auf der Liste ankommt.
